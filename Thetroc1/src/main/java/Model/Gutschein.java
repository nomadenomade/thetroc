package Model;

public class Gutschein {
	private int idgutschein;
	private String pseudokaufer;
	private String emailkaufer;
	private String nameprodukt;
	private String betrag;
	private String datumguschein;
	private int idverkaufer;
	private int idwarenkob;
	public int getIdgutschein() {
		return idgutschein;
	}
	public void setIdgutschein(int idgutschein) {
		this.idgutschein = idgutschein;
	}
	public String getPseudokaufer() {
		return pseudokaufer;
	}
	public void setPseudokaufer(String pseudokaufer) {
		this.pseudokaufer = pseudokaufer;
	}
	public String getEmailkaufer() {
		return emailkaufer;
	}
	public void setEmailkaufer(String emailkaufer) {
		this.emailkaufer = emailkaufer;
	}
	public String getNameprodukt() {
		return nameprodukt;
	}
	public void setNameprodukt(String nameprodukt) {
		this.nameprodukt = nameprodukt;
	}
	public String getBetrag() {
		return betrag;
	}
	public void setBetrag(String betrag) {
		this.betrag = betrag;
	}
	public String getDatumguschein() {
		return datumguschein;
	}
	public void setDatumguschein(String datumguschein) {
		this.datumguschein = datumguschein;
	}
	public int getIdverkaufer() {
		return idverkaufer;
	}
	public void setIdverkaufer(int idverkaufer) {
		this.idverkaufer = idverkaufer;
	}
	public int getIdwarenkob() {
		return idwarenkob;
	}
	public void setIdwarenkob(int idwarenkob) {
		this.idwarenkob = idwarenkob;
	}
	
	

}
